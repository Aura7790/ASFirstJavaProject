public class Calculator {
    public int calculateSum(int first, int second) {
        return first + second;
    }

    public double calculateSum(double first, double second, double third) {
        return first + second + third;

    }

    public float calculateSum(float first, float second) {
        return first + second;
    }

    public int calculateSubstraction(int first, int second, int third, int fourth) {
        return first - second - third - fourth;
    }

    public float calculateSubstraction(float first, float second) {
        return first - second;
    }

    public double calculateSubstraction(double first, double second) {
        return first - second;
    }

    public int calculateMultiplication(int first, int second) {
        return first * second;
    }

    public float calculateMultiplication(float first, float second, float third, float fourth) {
        return first * second * third * fourth;
    }

    public double calculateMultiplication(double first, double second) {
        return first * second;
    }

    public float calculateDivision(float first, float second) {
       return first / second;
    }

    public float calculateDivision(int first, int second, int third) {
        return  (float) first / second / third;
    }

    public double calculateDivision(double first, double second) {
        return first / second;
    }
}
